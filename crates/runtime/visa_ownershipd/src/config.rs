    fn loads_exact_jcs_and_builds_create_or_reopen_bootstrap_with_fresh_nonce() {
        let bytes = canonical(&document(StoreOpenPolicy::CreateIfMissingExact));
        let file = write_private(&bytes);
        let loaded = load_digest_pinned(file.path(), &pin(&bytes)).expect("load bootstrap");

        assert_eq!(loaded.canonical_sha256, <[u8; 32]>::from(Sha256::digest(&bytes)));
        assert_eq!(loaded.runtime.allowed_agent(AgentRole::Source).binding.role, AgentRole::Source);
        assert_eq!(
            loaded.runtime.allowed_agent(AgentRole::Destination).executable_sha256,
            [0x9a; 32]
        );
        assert!(loaded.runtime.receipt_authenticator().is_ok());

        let create = loaded
            .runtime
            .store_bootstrap(ProcessNonce::from_u128(100), Some(loaded.runtime.ownership_identity))
            .expect("create bootstrap");
        assert_eq!(create.create_identity, Some(loaded.runtime.ownership_identity));
        assert_eq!(create.process_nonce, ProcessNonce::from_u128(100));

        let reopen = loaded
            .runtime
            .store_bootstrap(ProcessNonce::from_u128(101), None)
            .expect("reopen bootstrap");
        assert_eq!(reopen.create_identity, None);
        assert_eq!(reopen.process_nonce, ProcessNonce::from_u128(101));
    }

