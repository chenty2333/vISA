#[derive(Clone, Copy, Debug, PartialEq, Eq)]
struct LowerHex<const N: usize>([u8; N]);

impl<const N: usize> Serialize for LowerHex<N> {
    fn serialize<S>(&self, serializer: S) -> Result<S::Ok, S::Error>
    where
        S: Serializer,
    {
        serializer.serialize_str(&encode_lower_hex(&self.0))
    }
}

impl<'de, const N: usize> Deserialize<'de> for LowerHex<N> {
    fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>
    where
        D: Deserializer<'de>,
    {
        let value = String::deserialize(deserializer)?;
        parse_lower_hex(&value).map(Self).map_err(de::Error::custom)
    }
}

fn parse_lower_hex<const N: usize>(value: &str) -> Result<[u8; N], &'static str> {
    if value.len() != N.checked_mul(2).ok_or("hex length overflow")? {
        return Err("wrong lowercase hex length");
    }
    let mut output = [0_u8; N];
    for (index, pair) in value.as_bytes().chunks_exact(2).enumerate() {
        output[index] = decode_lower_nibble(pair[0])? * 16 + decode_lower_nibble(pair[1])?;
    }
    Ok(output)
}

fn decode_lower_nibble(value: u8) -> Result<u8, &'static str> {
    match value {
        b'0'..=b'9' => Ok(value - b'0'),
        b'a'..=b'f' => Ok(value - b'a' + 10),
        _ => Err("hex must use lowercase ASCII digits"),
    }
}

fn encode_lower_hex(bytes: &[u8]) -> String {
    const DIGITS: &[u8; 16] = b"0123456789abcdef";
    let mut encoded = String::with_capacity(bytes.len() * 2);
    for byte in bytes {
        encoded.push(char::from(DIGITS[usize::from(byte >> 4)]));
        encoded.push(char::from(DIGITS[usize::from(byte & 0x0f)]));
    }
    encoded
}

