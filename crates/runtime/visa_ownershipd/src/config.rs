#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
struct StoreLimitsDocument {
    max_exchanges: u64,
    max_exchange_bytes: u64,
    max_database_bytes: u64,
}

impl StoreLimitsDocument {
    fn into_typed(self) -> Result<StoreLimits, ConfigError> {
        if self.max_exchanges == 0
            || self.max_exchange_bytes < 2 * MAX_INNER_REQUEST_BYTES as u64
            || self.max_database_bytes < 1024 * 1024
            || !self.max_database_bytes.is_multiple_of(SQLITE_PAGE_SIZE)
            || self.max_exchanges > i64::MAX as u64
            || self.max_exchange_bytes > i64::MAX as u64
            || self.max_database_bytes > i64::MAX as u64
        {
            return Err(ConfigError::InvalidField("store limits"));
        }
        Ok(StoreLimits {
            max_exchanges: self.max_exchanges,
            max_exchange_bytes: self.max_exchange_bytes,
            max_database_bytes: self.max_database_bytes,
        })
    }
}

