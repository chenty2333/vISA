//! Digest-pinned, non-authoritative bootstrap input for `visa-ownershipd`.
//!
//! The bootstrap file is an explicit launch input, not a release trust root.
//! Release installation/provenance and exact-tag evidence remain separate
//! gates. The daemon accepts this input only when its exact RFC 8785 JCS bytes
//! match a lowercase SHA-256 supplied separately on argv.

