/// Exact schema accepted by this O2 bootstrap reader.
pub const BOOTSTRAP_SCHEMA: &str = "visa.ownershipd.bootstrap.v1";

/// Hard cap applied before JSON parsing.
pub const MAX_BOOTSTRAP_BYTES: usize = 1024 * 1024;

const SQLITE_PAGE_SIZE: u64 = 4096;
const MAX_DATABASE_PATH_BYTES: usize = 4096;

/// Whether this process may create the exact configured store when it is absent.
///
/// `CreateIfMissingExact` does not perform adoption. The caller must first use
/// `symlink_metadata`: only `NotFound` may select O1 create-new, while an existing
/// path selects reopen and every create/reopen race fails in O1.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum StoreOpenPolicy {
    /// Create with the pinned identity only after an exact `NotFound` observation.
    CreateIfMissingExact,
    /// Refuse creation and reopen the already initialized store only.
    ReopenExisting,
}

/// Exact process identity and executable digest admitted for one agent role.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub struct PinnedAgent {
    pub binding: AgentBinding,
    pub executable_sha256: [u8; 32],
}

/// Typed bootstrap values used to construct the O1 store and peer-admission policy.
///
/// This value is a digest-pinned launch input. It is intentionally not described
/// as a release trust root or as executable provenance.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct RuntimeConfig {
    pub store_open_policy: StoreOpenPolicy,
    pub database_path: PathBuf,
    pub store_binding: StoreBinding,
    pub ownership_identity: OwnershipServiceIdentity,
    pub store_limits: StoreLimits,
    pub joint_issuers: JointIssuerSet,
    pub source_agent: PinnedAgent,
    pub destination_agent: PinnedAgent,
}

impl RuntimeConfig {
    /// Returns the exact configured identity for an agent role.
    pub const fn allowed_agent(&self, role: AgentRole) -> &PinnedAgent {
        match role {
            AgentRole::Source => &self.source_agent,
            AgentRole::Destination => &self.destination_agent,
        }
    }

    /// Builds an O1 bootstrap using the fresh nonce generated by this process.
    ///
    /// `create_identity` must be `None` for reopen, or the exact pinned identity
    /// after a caller has observed `NotFound` under `CreateIfMissingExact`.
    pub fn store_bootstrap(
        &self,
        process_nonce: ProcessNonce,
        create_identity: Option<OwnershipServiceIdentity>,
    ) -> Result<StoreBootstrap, ConfigError> {
        process_nonce
            .validate()
            .map_err(|_| ConfigError::InvalidField("ownership process nonce"))?;
        if create_identity.is_some_and(|identity| identity != self.ownership_identity) {
            return Err(ConfigError::InvalidField("ownership create identity"));
        }
        if self.store_open_policy == StoreOpenPolicy::ReopenExisting && create_identity.is_some() {
            return Err(ConfigError::InvalidField("store open policy"));
        }
        Ok(StoreBootstrap {
            binding: self.store_binding,
            create_identity,
            process_nonce,
            limits: self.store_limits,
        })
    }

    /// Reconstructs the validated same-UID local receipt policy.
    pub fn receipt_authenticator(&self) -> Result<PinnedLocalReceiptAuthenticator, ConfigError> {
        PinnedLocalReceiptAuthenticator::new(self.joint_issuers)
            .ok_or(ConfigError::InvalidField("joint issuers"))
    }
}

/// A validated runtime configuration and the digest of its exact canonical bytes.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct LoadedRuntimeConfig {
    pub runtime: RuntimeConfig,
    pub canonical_sha256: [u8; 32],
}

/// Bootstrap loading failure.
#[derive(Debug)]
pub enum ConfigError {
    InvalidDigestPin,
    Io { operation: &'static str, source: io::Error },
    InsecureFile,
    TooLarge,
    SizeChanged,
    DigestMismatch { expected: [u8; 32], observed: [u8; 32] },
    Json(serde_json::Error),
    NonCanonical,
    WrongSchema,
    InvalidField(&'static str),
}

impl fmt::Display for ConfigError {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::InvalidDigestPin => {
                formatter.write_str("bootstrap SHA-256 argv pin is not 64 lowercase hex digits")
            }
            Self::Io { operation, source } => {
                write!(formatter, "cannot {operation} bootstrap file: {source}")
            }
            Self::InsecureFile => formatter
                .write_str("bootstrap must be one regular euid-owned single-link mode-0600 file"),
            Self::TooLarge => {
                write!(formatter, "bootstrap exceeds the {MAX_BOOTSTRAP_BYTES}-byte input limit")
            }
            Self::SizeChanged => {
                formatter.write_str("bootstrap size changed while it was being read")
            }
            Self::DigestMismatch { expected, observed } => write!(
                formatter,
                "bootstrap SHA-256 mismatch: expected {}, observed {}",
                encode_lower_hex(expected),
                encode_lower_hex(observed)
            ),
            Self::Json(source) => write!(formatter, "bootstrap JSON is invalid: {source}"),
            Self::NonCanonical => formatter.write_str("bootstrap is not exact RFC 8785 JCS JSON"),
            Self::WrongSchema => {
                write!(formatter, "bootstrap schema must be exactly {BOOTSTRAP_SCHEMA}")
            }
            Self::InvalidField(field) => write!(formatter, "invalid bootstrap {field}"),
        }
    }
}

impl std::error::Error for ConfigError {
    fn source(&self) -> Option<&(dyn std::error::Error + 'static)> {
        match self {
            Self::Io { source, .. } => Some(source),
            Self::Json(source) => Some(source),
            Self::InvalidDigestPin
            | Self::InsecureFile
            | Self::TooLarge
            | Self::SizeChanged
            | Self::DigestMismatch { .. }
            | Self::NonCanonical
            | Self::WrongSchema
            | Self::InvalidField(_) => None,
        }
    }
}

/// Securely loads one exact canonical bootstrap file pinned by an argv digest.
pub fn load_digest_pinned(
    path: impl AsRef<Path>,
    expected_sha256_argv: &str,
) -> Result<LoadedRuntimeConfig, ConfigError> {
    let expected =
        parse_lower_hex::<32>(expected_sha256_argv).map_err(|_| ConfigError::InvalidDigestPin)?;
    let bytes = read_secure_bootstrap(path.as_ref())?;
    let observed: [u8; 32] = Sha256::digest(&bytes).into();
    if observed != expected {
        return Err(ConfigError::DigestMismatch { expected, observed });
    }

    let document: BootstrapDocument = serde_json::from_slice(&bytes).map_err(ConfigError::Json)?;
    let canonical = to_jcs_vec(&document).map_err(ConfigError::Json)?;
    if canonical != bytes {
        return Err(ConfigError::NonCanonical);
    }
    let runtime = document.into_runtime()?;
    Ok(LoadedRuntimeConfig { runtime, canonical_sha256: observed })
}

fn read_secure_bootstrap(path: &Path) -> Result<Vec<u8>, ConfigError> {
    let descriptor = open(
        path,
        OFlags::RDONLY | OFlags::CLOEXEC | OFlags::NOFOLLOW | OFlags::NONBLOCK,
        Mode::empty(),
    )
    .map_err(|source| ConfigError::Io { operation: "open", source: source.into() })?;
    let initial_size = verify_private_regular_fd(&descriptor)?;
    if initial_size > MAX_BOOTSTRAP_BYTES as u64 {
        return Err(ConfigError::TooLarge);
    }

    let mut file: File = descriptor.into();
    let mut bytes = Vec::with_capacity(initial_size as usize);
    (&mut file)
        .take(MAX_BOOTSTRAP_BYTES as u64 + 1)
        .read_to_end(&mut bytes)
        .map_err(|source| ConfigError::Io { operation: "read", source })?;
    if bytes.len() > MAX_BOOTSTRAP_BYTES {
        return Err(ConfigError::TooLarge);
    }
    let final_size = verify_private_regular_fd(&file)?;
    if initial_size != final_size || final_size != bytes.len() as u64 {
        return Err(ConfigError::SizeChanged);
    }
    Ok(bytes)
}

fn verify_private_regular_fd(fd: &impl std::os::fd::AsFd) -> Result<u64, ConfigError> {
    let metadata = fstat(fd)
        .map_err(|source| ConfigError::Io { operation: "inspect", source: source.into() })?;
    let permissions = Mode::from_raw_mode(metadata.st_mode)
        & (Mode::RWXU | Mode::RWXG | Mode::RWXO | Mode::SUID | Mode::SGID | Mode::SVTX);
    if FileType::from_raw_mode(metadata.st_mode) != FileType::RegularFile
        || metadata.st_uid != geteuid().as_raw()
        || metadata.st_nlink != 1
        || permissions != Mode::RUSR | Mode::WUSR
    {
        return Err(ConfigError::InsecureFile);
    }
    u64::try_from(metadata.st_size).map_err(|_| ConfigError::InsecureFile)
}

#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
struct BootstrapDocument {
    schema: String,
    store_open_policy: StoreOpenPolicy,
    database_path: String,
    store_binding: StoreBindingDocument,
    ownership_identity: OwnershipIdentityDocument,
    store_limits: StoreLimitsDocument,
    joint_issuers: JointIssuerSetDocument,
    source_agent: AgentDocument,
    destination_agent: AgentDocument,
}

impl BootstrapDocument {
    fn into_runtime(self) -> Result<RuntimeConfig, ConfigError> {
        if self.schema != BOOTSTRAP_SCHEMA {
            return Err(ConfigError::WrongSchema);
        }
        let database_path = validate_database_path(&self.database_path)?;
        let store_binding = self.store_binding.into_typed()?;
        let ownership_identity = self.ownership_identity.into_typed()?;
        let store_limits = self.store_limits.into_typed()?;
        let joint_issuers = self.joint_issuers.into_typed();
        let authenticator = PinnedLocalReceiptAuthenticator::new(joint_issuers)
            .ok_or(ConfigError::InvalidField("joint issuers"))?;
        let expected_ownership_issuer = ReceiptIssuerIdentity {
            issuer: Identity::from_bytes(ownership_identity.issuer.0),
            issuer_incarnation: Identity::from_bytes(ownership_identity.service_incarnation.0),
            key_id: Identity::from_bytes(ownership_identity.key_id.0),
            log_id: Identity::from_bytes(ownership_identity.log_namespace.0),
        };
        if authenticator.ownership_namespace() != expected_ownership_issuer {
            return Err(ConfigError::InvalidField("ownership issuer binding"));
        }

        let source_agent = self.source_agent.into_typed()?;
        let destination_agent = self.destination_agent.into_typed()?;
        validate_agent(&source_agent, AgentRole::Source, store_binding)?;
        validate_agent(&destination_agent, AgentRole::Destination, store_binding)?;

        Ok(RuntimeConfig {
            store_open_policy: self.store_open_policy,
            database_path,
            store_binding,
            ownership_identity,
            store_limits,
            joint_issuers,
            source_agent,
            destination_agent,
        })
    }
}

fn validate_database_path(value: &str) -> Result<PathBuf, ConfigError> {
    if value.is_empty() || value.len() > MAX_DATABASE_PATH_BYTES || value.as_bytes().contains(&0) {
        return Err(ConfigError::InvalidField("database path"));
    }
    let path = PathBuf::from(value);
    if !path.is_absolute()
        || path.file_name().is_none()
        || !path.components().all(|part| matches!(part, Component::RootDir | Component::Normal(_)))
    {
        return Err(ConfigError::InvalidField("database path"));
    }
    Ok(path)
}

fn validate_agent(
    agent: &PinnedAgent,
    expected_role: AgentRole,
    store_binding: StoreBinding,
) -> Result<(), ConfigError> {
    agent.binding.validate().map_err(|_| ConfigError::InvalidField("agent binding"))?;
    if agent.binding.role != expected_role {
        return Err(ConfigError::InvalidField("agent role"));
    }
    if agent.binding.cohort != store_binding.cohort
        || agent.binding.boot != store_binding.boot
        || agent.binding.runtime_session != store_binding.runtime_session
    {
        return Err(ConfigError::InvalidField("agent store binding"));
    }
    if agent.executable_sha256 == [0; 32] {
        return Err(ConfigError::InvalidField("agent executable sha256"));
    }
    Ok(())
}

#[derive(Clone, Copy, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
struct StoreBindingDocument {
    cohort: LowerHex<16>,
    boot: LowerHex<16>,
    runtime_session: LowerHex<16>,
}

impl StoreBindingDocument {
    fn into_typed(self) -> Result<StoreBinding, ConfigError> {
        let binding = StoreBinding {
            cohort: CohortId::from_bytes(self.cohort.0),
            boot: BootId::from_bytes(self.boot.0),
            runtime_session: RuntimeSessionId::from_bytes(self.runtime_session.0),
        };
        binding
            .cohort
            .validate()
            .and_then(|()| binding.boot.validate())
            .and_then(|()| binding.runtime_session.validate())
            .map_err(|_| ConfigError::InvalidField("store binding"))?;
        Ok(binding)
    }
}

