    #[test]
    fn rejects_insecure_symlink_hardlink_mode_and_oversize_inputs() {
        let bytes = canonical(&document(StoreOpenPolicy::CreateIfMissingExact));

        let mode = write_private(&bytes);
        fs::set_permissions(mode.path(), fs::Permissions::from_mode(0o640)).expect("weaken mode");
        assert!(matches!(
            load_digest_pinned(mode.path(), &pin(&bytes)),
            Err(ConfigError::InsecureFile)
        ));

        let special_mode = write_private(&bytes);
        fs::set_permissions(special_mode.path(), fs::Permissions::from_mode(0o4600))
            .expect("set special mode");
        assert!(matches!(
            load_digest_pinned(special_mode.path(), &pin(&bytes)),
            Err(ConfigError::InsecureFile)
        ));

        let source = write_private(&bytes);
        let root = tempdir().expect("temporary link directory");
        let hardlink = root.path().join("hardlink.json");
        fs::hard_link(source.path(), &hardlink).expect("create hardlink");
        assert!(matches!(
            load_digest_pinned(source.path(), &pin(&bytes)),
            Err(ConfigError::InsecureFile)
        ));

        let target = write_private(&bytes);
        let symlink_path = root.path().join("symlink.json");
        symlink(target.path(), &symlink_path).expect("create symlink");
        assert!(matches!(
            load_digest_pinned(&symlink_path, &pin(&bytes)),
            Err(ConfigError::Io { operation: "open", .. })
        ));

        let oversized = vec![b' '; MAX_BOOTSTRAP_BYTES + 1];
        let file = write_private(&oversized);
        assert!(matches!(
            load_digest_pinned(file.path(), &pin(&oversized)),
            Err(ConfigError::TooLarge)
        ));
    }
