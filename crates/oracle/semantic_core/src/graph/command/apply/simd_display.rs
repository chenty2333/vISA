use alloc::boxed::Box;

use super::*;

pub(super) fn apply_simd_display_command(
    graph: &mut SemanticGraph,
    command: SemanticCommand,
) -> ApplyDispatch {
    let applied = match command {
        SemanticCommand::RecordTargetFeatureSet {
            feature_set,
            name,
            discovery_source,
            target_profile,
            target_arch,
            base_isa,
            simd_abi,
            simd_supported,
            vector_register_count,
            vector_register_bits,
            scalar_fallback,
            unsupported_reason,
            note,
        } => graph.record_target_feature_set_with_id(
            feature_set,
            &name,
            &discovery_source,
            &target_profile,
            &target_arch,
            &base_isa,
            &simd_abi,
            simd_supported,
            vector_register_count,
            vector_register_bits,
            scalar_fallback,
            &unsupported_reason,
            &note,
        ),
        SemanticCommand::RecordVectorState {
            vector_state,
            owner_activation,
            owner_store,
            code_object,
            target_feature_set,
            simd_abi,
            vector_register_count,
            vector_register_bits,
            register_bytes,
            state,
            note,
        } => graph.record_vector_state_with_id(
            vector_state,
            owner_activation,
            owner_store,
            code_object,
            target_feature_set,
            &simd_abi,
            vector_register_count,
            vector_register_bits,
            register_bytes,
            state,
            &note,
        ),
        SemanticCommand::RecordSimdFaultInjection {
            injection,
            activation,
            code_object,
            trap,
            target_feature_set,
            vector_state,
            kind,
            effect,
            required_abi,
            vector_register_count,
            vector_register_bits,
            injected_faults,
            note,
        } => graph.record_simd_fault_injection_with_id(
            injection,
            activation,
            code_object,
            trap,
            target_feature_set,
            vector_state,
            kind,
            effect,
            &required_abi,
            vector_register_count,
            vector_register_bits,
            injected_faults,
            &note,
        ),
        SemanticCommand::RecordSimdBenchmark {
            benchmark,
            target_feature_set,
            scalar_code_object,
            vector_code_object,
            simd_abi,
            vector_register_count,
            vector_register_bits,
            workload_units,
            scalar_nanos,
            vector_nanos,
            speedup_milli,
            context_overhead_nanos,
            note,
        } => graph.record_simd_benchmark_with_id(
            benchmark,
            target_feature_set,
            scalar_code_object,
            vector_code_object,
            &simd_abi,
            vector_register_count,
            vector_register_bits,
            workload_units,
            scalar_nanos,
            vector_nanos,
            speedup_milli,
            context_overhead_nanos,
            &note,
        ),
        SemanticCommand::RecordSimdContextSwitchBenchmark {
            benchmark,
            preemption,
            activation_resume,
            saved_vector_state,
            restored_vector_state,
            target_feature_set,
            simd_abi,
            vector_register_count,
            vector_register_bits,
            sample_count,
            scalar_context_switch_nanos,
            vector_context_switch_nanos,
            overhead_nanos,
            budget_nanos,
            note,
        } => graph.record_simd_context_switch_benchmark_with_id(
            benchmark,
            preemption,
            activation_resume,
            saved_vector_state,
            restored_vector_state,
            target_feature_set,
            &simd_abi,
            vector_register_count,
            vector_register_bits,
            sample_count,
            scalar_context_switch_nanos,
            vector_context_switch_nanos,
            overhead_nanos,
            budget_nanos,
            &note,
        ),
        SemanticCommand::RecordFramebufferObject {
            framebuffer,
            name,
            resource,
            resource_generation,
            width,
            height,
            stride_bytes,
            pixel_format,
            byte_len,
            note,
        } => graph.record_framebuffer_object_with_id(
            framebuffer,
            &name,
            resource,
            resource_generation,
            width,
            height,
            stride_bytes,
            &pixel_format,
            byte_len,
            &note,
        ),
        SemanticCommand::RecordDisplayObject {
            display,
            name,
            framebuffer,
            framebuffer_generation,
            mode_name,
            width,
            height,
            refresh_millihz,
            note,
        } => graph.record_display_object_with_id(
            display,
            &name,
            framebuffer,
            framebuffer_generation,
            &mode_name,
            width,
            height,
            refresh_millihz,
            &note,
        ),
        SemanticCommand::RecordDisplayCapability {
            display_capability,
            owner_store,
            owner_store_generation,
            display,
            display_generation,
            capability,
            capability_generation,
            handle,
            operations,
            note,
        } => graph.record_display_capability_with_id(
            display_capability,
            owner_store,
            owner_store_generation,
            display,
            display_generation,
            capability,
            capability_generation,
            handle,
            operations,
            &note,
        ),
        SemanticCommand::RecordFramebufferWindowLease {
            framebuffer_window_lease,
            owner_store,
            owner_store_generation,
            display_capability,
            display_capability_generation,
            display,
            display_generation,
            framebuffer,
            framebuffer_generation,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            access,
            note,
        } => graph.record_framebuffer_window_lease_with_id(
            framebuffer_window_lease,
            owner_store,
            owner_store_generation,
            display_capability,
            display_capability_generation,
            display,
            display_generation,
            framebuffer,
            framebuffer_generation,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            &access,
            &note,
        ),
        SemanticCommand::RecordFramebufferMapping {
            framebuffer_mapping,
            owner_store,
            owner_store_generation,
            framebuffer_window_lease,
            framebuffer_window_lease_generation,
            map_handle_slot,
            map_handle_generation,
            map_handle_tag,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            access,
            mode,
            note,
        } => graph.record_framebuffer_mapping_with_id(
            framebuffer_mapping,
            owner_store,
            owner_store_generation,
            framebuffer_window_lease,
            framebuffer_window_lease_generation,
            map_handle_slot,
            map_handle_generation,
            map_handle_tag,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            &access,
            &mode,
            &note,
        ),
        SemanticCommand::RecordFramebufferWrite {
            framebuffer_write,
            owner_store,
            owner_store_generation,
            framebuffer_mapping,
            framebuffer_mapping_generation,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            payload_digest,
            note,
        } => graph.record_framebuffer_write_with_id(
            framebuffer_write,
            owner_store,
            owner_store_generation,
            framebuffer_mapping,
            framebuffer_mapping_generation,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            payload_digest,
            &note,
        ),
        SemanticCommand::RecordFramebufferFlushRegion {
            framebuffer_flush_region,
            owner_store,
            owner_store_generation,
            framebuffer_write,
            framebuffer_write_generation,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            payload_digest,
            note,
        } => graph.record_framebuffer_flush_region_with_id(
            framebuffer_flush_region,
            owner_store,
            owner_store_generation,
            framebuffer_write,
            framebuffer_write_generation,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            payload_digest,
            &note,
        ),
        SemanticCommand::RecordFramebufferDirtyRegion {
            framebuffer_dirty_region,
            owner_store,
            owner_store_generation,
            framebuffer_write,
            framebuffer_write_generation,
            framebuffer_flush_region,
            framebuffer_flush_region_generation,
            state,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            payload_digest,
            note,
        } => graph.record_framebuffer_dirty_region_with_id(
            framebuffer_dirty_region,
            owner_store,
            owner_store_generation,
            framebuffer_write,
            framebuffer_write_generation,
            framebuffer_flush_region,
            framebuffer_flush_region_generation,
            state,
            x,
            y,
            width,
            height,
            byte_offset,
            byte_len,
            payload_digest,
            &note,
        ),
        SemanticCommand::RecordDisplayEventLog {
            display_event_log,
            owner_store,
            owner_store_generation,
            framebuffer_dirty_region,
            framebuffer_dirty_region_generation,
            first_event,
            last_event,
            event_count,
            flush_count,
            dirty_region_count,
            note,
        } => graph.record_display_event_log_with_id(
            display_event_log,
            owner_store,
            owner_store_generation,
            framebuffer_dirty_region,
            framebuffer_dirty_region_generation,
            first_event,
            last_event,
            event_count,
            flush_count,
            dirty_region_count,
            &note,
        ),
        SemanticCommand::CleanupDisplay {
            cleanup,
            owner_store,
            owner_store_generation,
            display_capability,
            display_capability_generation,
            display,
            display_generation,
            framebuffer,
            framebuffer_generation,
            reason,
            note,
        } => graph.cleanup_display_for_store_with_id(
            cleanup,
            owner_store,
            owner_store_generation,
            display_capability,
            display_capability_generation,
            display,
            display_generation,
            framebuffer,
            framebuffer_generation,
            &reason,
            &note,
        ),
        SemanticCommand::ValidateDisplaySnapshotBarrier {
            barrier,
            owner_store,
            owner_store_generation,
            display,
            display_generation,
            framebuffer,
            framebuffer_generation,
            display_cleanup,
            display_cleanup_generation,
            reason,
            note,
        } => graph.validate_display_snapshot_barrier_with_id(
            barrier,
            owner_store,
            owner_store_generation,
            display,
            display_generation,
            framebuffer,
            framebuffer_generation,
            display_cleanup,
            display_cleanup_generation,
            &reason,
            &note,
        ),
        SemanticCommand::RecordDisplayPanicLastFrame {
            panic_last_frame,
            owner_store,
            owner_store_generation,
            display_snapshot_barrier,
            display_snapshot_barrier_generation,
            display_event_log,
            display_event_log_generation,
            framebuffer_write,
            framebuffer_write_generation,
            framebuffer_flush_region,
            framebuffer_flush_region_generation,
            payload_digest,
            summary_digest,
            summary_record_bytes,
            panic_epoch,
            panic_record_kind,
            raw_framebuffer_bytes_exported,
            note,
        } => graph.record_display_panic_last_frame_with_id(
            panic_last_frame,
            owner_store,
            owner_store_generation,
            display_snapshot_barrier,
            display_snapshot_barrier_generation,
            display_event_log,
            display_event_log_generation,
            framebuffer_write,
            framebuffer_write_generation,
            framebuffer_flush_region,
            framebuffer_flush_region_generation,
            payload_digest,
            summary_digest,
            summary_record_bytes,
            panic_epoch,
            &panic_record_kind,
            raw_framebuffer_bytes_exported,
            &note,
        ),
        SemanticCommand::RecordFramebufferBenchmark {
            benchmark,
            scenario,
            owner_store,
            owner_store_generation,
            display_capability,
            display_capability_generation,
            framebuffer_write,
            framebuffer_write_generation,
            framebuffer_flush_region,
            framebuffer_flush_region_generation,
            display_event_log,
            display_event_log_generation,
            display_snapshot_barrier,
            display_snapshot_barrier_generation,
            sample_frames,
            sample_bytes,
            frame_area_pixels,
            write_nanos,
            flush_nanos,
            measured_nanos,
            budget_nanos,
            p50_latency_nanos,
            p99_latency_nanos,
            note,
        } => graph.record_framebuffer_benchmark_with_id(
            benchmark,
            &scenario,
            owner_store,
            owner_store_generation,
            display_capability,
            display_capability_generation,
            framebuffer_write,
            framebuffer_write_generation,
            framebuffer_flush_region,
            framebuffer_flush_region_generation,
            display_event_log,
            display_event_log_generation,
            display_snapshot_barrier,
            display_snapshot_barrier_generation,
            sample_frames,
            sample_bytes,
            frame_area_pixels,
            write_nanos,
            flush_nanos,
            measured_nanos,
            budget_nanos,
            p50_latency_nanos,
            p99_latency_nanos,
            &note,
        ),
        other => return ApplyDispatch::Next(Box::new(other)),
    };
    ApplyDispatch::Applied(applied)
}
