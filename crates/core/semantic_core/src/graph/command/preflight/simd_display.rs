use super::*;

impl SemanticGraph {
    pub(super) fn preflight_simd_display_command(
        &self,
        command: &SemanticCommand,
    ) -> Result<(), CommandError> {
        match command {
            SemanticCommand::RecordTargetFeatureSet {
                feature_set,
                name,
                discovery_source,
                target_profile,
                target_arch,
                base_isa,
                simd_abi,
                simd_supported,
                vector_register_count,
                vector_register_bits,
                scalar_fallback,
                unsupported_reason,
                ..
            } => self
                .validate_target_feature_set(
                    *feature_set,
                    name,
                    discovery_source,
                    target_profile,
                    target_arch,
                    base_isa,
                    simd_abi,
                    *simd_supported,
                    *vector_register_count,
                    *vector_register_bits,
                    *scalar_fallback,
                    unsupported_reason,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordVectorState {
                vector_state,
                owner_activation,
                owner_store,
                code_object,
                target_feature_set,
                simd_abi,
                vector_register_count,
                vector_register_bits,
                register_bytes,
                state,
                ..
            } => self
                .validate_vector_state(
                    *vector_state,
                    *owner_activation,
                    *owner_store,
                    *code_object,
                    *target_feature_set,
                    simd_abi,
                    *vector_register_count,
                    *vector_register_bits,
                    *register_bytes,
                    *state,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordSimdFaultInjection {
                injection,
                activation,
                code_object,
                trap,
                target_feature_set,
                vector_state,
                kind,
                effect,
                required_abi,
                vector_register_count,
                vector_register_bits,
                injected_faults,
                ..
            } => self
                .validate_simd_fault_injection(
                    *injection,
                    *activation,
                    *code_object,
                    *trap,
                    *target_feature_set,
                    *vector_state,
                    *kind,
                    *effect,
                    required_abi,
                    *vector_register_count,
                    *vector_register_bits,
                    *injected_faults,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordSimdBenchmark {
                benchmark,
                target_feature_set,
                scalar_code_object,
                vector_code_object,
                simd_abi,
                vector_register_count,
                vector_register_bits,
                workload_units,
                scalar_nanos,
                vector_nanos,
                speedup_milli,
                context_overhead_nanos,
                ..
            } => {
                if self.simd_benchmarks.iter().any(|record| record.id == *benchmark) {
                    Err(CommandError::precondition("SIMD benchmark already exists"))
                } else {
                    self.validate_simd_benchmark(
                        *benchmark,
                        *target_feature_set,
                        *scalar_code_object,
                        *vector_code_object,
                        simd_abi,
                        *vector_register_count,
                        *vector_register_bits,
                        *workload_units,
                        *scalar_nanos,
                        *vector_nanos,
                        *speedup_milli,
                        *context_overhead_nanos,
                    )
                    .map_err(CommandError::precondition)
                }
            }
            SemanticCommand::RecordSimdContextSwitchBenchmark {
                benchmark,
                preemption,
                activation_resume,
                saved_vector_state,
                restored_vector_state,
                target_feature_set,
                simd_abi,
                vector_register_count,
                vector_register_bits,
                sample_count,
                scalar_context_switch_nanos,
                vector_context_switch_nanos,
                overhead_nanos,
                budget_nanos,
                ..
            } => {
                if self.simd_context_switch_benchmarks.iter().any(|record| record.id == *benchmark)
                {
                    Err(CommandError::precondition("SIMD context switch benchmark already exists"))
                } else {
                    self.validate_simd_context_switch_benchmark(
                        *benchmark,
                        *preemption,
                        *activation_resume,
                        *saved_vector_state,
                        *restored_vector_state,
                        *target_feature_set,
                        simd_abi,
                        *vector_register_count,
                        *vector_register_bits,
                        *sample_count,
                        *scalar_context_switch_nanos,
                        *vector_context_switch_nanos,
                        *overhead_nanos,
                        *budget_nanos,
                    )
                    .map_err(CommandError::precondition)
                }
            }
            SemanticCommand::RecordFramebufferObject {
                framebuffer,
                name,
                resource,
                resource_generation,
                width,
                height,
                stride_bytes,
                pixel_format,
                byte_len,
                ..
            } => self
                .validate_framebuffer_object(
                    *framebuffer,
                    name,
                    *resource,
                    *resource_generation,
                    *width,
                    *height,
                    *stride_bytes,
                    pixel_format,
                    *byte_len,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordDisplayObject {
                display,
                name,
                framebuffer,
                framebuffer_generation,
                mode_name,
                width,
                height,
                refresh_millihz,
                ..
            } => self
                .validate_display_object(
                    *display,
                    name,
                    *framebuffer,
                    *framebuffer_generation,
                    mode_name,
                    *width,
                    *height,
                    *refresh_millihz,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordDisplayCapability {
                display_capability,
                owner_store,
                owner_store_generation,
                display,
                display_generation,
                capability,
                capability_generation,
                handle,
                operations,
                ..
            } => self
                .validate_display_capability(
                    *display_capability,
                    *owner_store,
                    *owner_store_generation,
                    *display,
                    *display_generation,
                    *capability,
                    *capability_generation,
                    handle,
                    operations,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordFramebufferWindowLease {
                framebuffer_window_lease,
                owner_store,
                owner_store_generation,
                display_capability,
                display_capability_generation,
                display,
                display_generation,
                framebuffer,
                framebuffer_generation,
                x,
                y,
                width,
                height,
                byte_offset,
                byte_len,
                access,
                ..
            } => self
                .validate_framebuffer_window_lease(
                    *framebuffer_window_lease,
                    *owner_store,
                    *owner_store_generation,
                    *display_capability,
                    *display_capability_generation,
                    *display,
                    *display_generation,
                    *framebuffer,
                    *framebuffer_generation,
                    *x,
                    *y,
                    *width,
                    *height,
                    *byte_offset,
                    *byte_len,
                    access,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordFramebufferMapping {
                framebuffer_mapping,
                owner_store,
                owner_store_generation,
                framebuffer_window_lease,
                framebuffer_window_lease_generation,
                map_handle_slot,
                map_handle_generation,
                map_handle_tag,
                x,
                y,
                width,
                height,
                byte_offset,
                byte_len,
                access,
                mode,
                ..
            } => self
                .validate_framebuffer_mapping(
                    *framebuffer_mapping,
                    *owner_store,
                    *owner_store_generation,
                    *framebuffer_window_lease,
                    *framebuffer_window_lease_generation,
                    *map_handle_slot,
                    *map_handle_generation,
                    *map_handle_tag,
                    *x,
                    *y,
                    *width,
                    *height,
                    *byte_offset,
                    *byte_len,
                    access,
                    mode,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordFramebufferWrite {
                framebuffer_write,
                owner_store,
                owner_store_generation,
                framebuffer_mapping,
                framebuffer_mapping_generation,
                x,
                y,
                width,
                height,
                byte_offset,
                byte_len,
                payload_digest,
                ..
            } => self
                .validate_framebuffer_write(
                    *framebuffer_write,
                    *owner_store,
                    *owner_store_generation,
                    *framebuffer_mapping,
                    *framebuffer_mapping_generation,
                    *x,
                    *y,
                    *width,
                    *height,
                    *byte_offset,
                    *byte_len,
                    *payload_digest,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordFramebufferFlushRegion {
                framebuffer_flush_region,
                owner_store,
                owner_store_generation,
                framebuffer_write,
                framebuffer_write_generation,
                x,
                y,
                width,
                height,
                byte_offset,
                byte_len,
                payload_digest,
                ..
            } => self
                .validate_framebuffer_flush_region(
                    *framebuffer_flush_region,
                    *owner_store,
                    *owner_store_generation,
                    *framebuffer_write,
                    *framebuffer_write_generation,
                    *x,
                    *y,
                    *width,
                    *height,
                    *byte_offset,
                    *byte_len,
                    *payload_digest,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordFramebufferDirtyRegion {
                framebuffer_dirty_region,
                owner_store,
                owner_store_generation,
                framebuffer_write,
                framebuffer_write_generation,
                framebuffer_flush_region,
                framebuffer_flush_region_generation,
                state,
                x,
                y,
                width,
                height,
                byte_offset,
                byte_len,
                payload_digest,
                ..
            } => self
                .validate_framebuffer_dirty_region(
                    *framebuffer_dirty_region,
                    *owner_store,
                    *owner_store_generation,
                    *framebuffer_write,
                    *framebuffer_write_generation,
                    *framebuffer_flush_region,
                    *framebuffer_flush_region_generation,
                    *state,
                    *x,
                    *y,
                    *width,
                    *height,
                    *byte_offset,
                    *byte_len,
                    *payload_digest,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordDisplayEventLog {
                display_event_log,
                owner_store,
                owner_store_generation,
                framebuffer_dirty_region,
                framebuffer_dirty_region_generation,
                first_event,
                last_event,
                event_count,
                flush_count,
                dirty_region_count,
                ..
            } => self
                .validate_display_event_log(
                    *display_event_log,
                    *owner_store,
                    *owner_store_generation,
                    *framebuffer_dirty_region,
                    *framebuffer_dirty_region_generation,
                    *first_event,
                    *last_event,
                    *event_count,
                    *flush_count,
                    *dirty_region_count,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::CleanupDisplay {
                cleanup,
                owner_store,
                owner_store_generation,
                display_capability,
                display_capability_generation,
                display,
                display_generation,
                framebuffer,
                framebuffer_generation,
                reason,
                ..
            } => self
                .validate_display_cleanup(
                    *cleanup,
                    *owner_store,
                    *owner_store_generation,
                    *display_capability,
                    *display_capability_generation,
                    *display,
                    *display_generation,
                    *framebuffer,
                    *framebuffer_generation,
                    reason,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::ValidateDisplaySnapshotBarrier {
                barrier,
                owner_store,
                owner_store_generation,
                display,
                display_generation,
                framebuffer,
                framebuffer_generation,
                display_cleanup,
                display_cleanup_generation,
                reason,
                ..
            } => self
                .validate_display_snapshot_barrier(
                    *barrier,
                    *owner_store,
                    *owner_store_generation,
                    *display,
                    *display_generation,
                    *framebuffer,
                    *framebuffer_generation,
                    *display_cleanup,
                    *display_cleanup_generation,
                    reason,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordDisplayPanicLastFrame {
                panic_last_frame,
                owner_store,
                owner_store_generation,
                display_snapshot_barrier,
                display_snapshot_barrier_generation,
                display_event_log,
                display_event_log_generation,
                framebuffer_write,
                framebuffer_write_generation,
                framebuffer_flush_region,
                framebuffer_flush_region_generation,
                payload_digest,
                summary_digest,
                summary_record_bytes,
                panic_epoch,
                panic_record_kind,
                raw_framebuffer_bytes_exported,
                ..
            } => self
                .validate_display_panic_last_frame(
                    *panic_last_frame,
                    *owner_store,
                    *owner_store_generation,
                    *display_snapshot_barrier,
                    *display_snapshot_barrier_generation,
                    *display_event_log,
                    *display_event_log_generation,
                    *framebuffer_write,
                    *framebuffer_write_generation,
                    *framebuffer_flush_region,
                    *framebuffer_flush_region_generation,
                    *payload_digest,
                    *summary_digest,
                    *summary_record_bytes,
                    *panic_epoch,
                    panic_record_kind,
                    *raw_framebuffer_bytes_exported,
                )
                .map_err(CommandError::precondition),
            SemanticCommand::RecordFramebufferBenchmark {
                benchmark,
                scenario,
                owner_store,
                owner_store_generation,
                display_capability,
                display_capability_generation,
                framebuffer_write,
                framebuffer_write_generation,
                framebuffer_flush_region,
                framebuffer_flush_region_generation,
                display_event_log,
                display_event_log_generation,
                display_snapshot_barrier,
                display_snapshot_barrier_generation,
                sample_frames,
                sample_bytes,
                frame_area_pixels,
                write_nanos,
                flush_nanos,
                measured_nanos,
                budget_nanos,
                p50_latency_nanos,
                p99_latency_nanos,
                ..
            } => self
                .validate_framebuffer_benchmark(
                    *benchmark,
                    scenario,
                    *owner_store,
                    *owner_store_generation,
                    *display_capability,
                    *display_capability_generation,
                    *framebuffer_write,
                    *framebuffer_write_generation,
                    *framebuffer_flush_region,
                    *framebuffer_flush_region_generation,
                    *display_event_log,
                    *display_event_log_generation,
                    *display_snapshot_barrier,
                    *display_snapshot_barrier_generation,
                    *sample_frames,
                    *sample_bytes,
                    *frame_area_pixels,
                    *write_nanos,
                    *flush_nanos,
                    *measured_nanos,
                    *budget_nanos,
                    *p50_latency_nanos,
                    *p99_latency_nanos,
                )
                .map_err(CommandError::precondition),
            _ => unreachable!("preflight handler called with wrong command domain"),
        }
    }
}
