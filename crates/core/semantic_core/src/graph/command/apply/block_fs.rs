use alloc::boxed::Box;

use super::*;

pub(super) fn apply_block_fs_command(
    graph: &mut SemanticGraph,
    command: SemanticCommand,
) -> ApplyDispatch {
    let applied = match command {
        SemanticCommand::RecordFakeBlockBackendObject {
            fake_block_backend,
            name,
            block_device,
            block_device_generation,
            provider,
            profile,
            sector_size,
            sector_count,
            read_only,
            max_transfer_sectors,
            deterministic_seed,
            note,
        } => graph.record_fake_block_backend_object_with_id(
            fake_block_backend,
            &name,
            block_device,
            block_device_generation,
            &provider,
            &profile,
            sector_size,
            sector_count,
            read_only,
            max_transfer_sectors,
            deterministic_seed,
            &note,
        ),
        SemanticCommand::RecordVirtioBlkBackendObject {
            virtio_blk_backend,
            name,
            block_device,
            block_device_generation,
            driver_binding,
            driver_binding_generation,
            provider,
            profile,
            model,
            sector_size,
            sector_count,
            read_only,
            max_transfer_sectors,
            device_features,
            driver_features,
            negotiated_features,
            request_queue_index,
            queue_size,
            irq_vector,
            note,
        } => graph.record_virtio_blk_backend_object_with_id(
            virtio_blk_backend,
            &name,
            block_device,
            block_device_generation,
            driver_binding,
            driver_binding_generation,
            &provider,
            &profile,
            &model,
            sector_size,
            sector_count,
            read_only,
            max_transfer_sectors,
            device_features,
            driver_features,
            negotiated_features,
            request_queue_index,
            queue_size,
            irq_vector,
            &note,
        ),
        SemanticCommand::RecordBlockReadPath {
            read_path,
            backend,
            block_request,
            block_request_generation,
            block_completion,
            block_completion_generation,
            data_digest,
            note,
        } => graph.record_block_read_path_with_id(
            read_path,
            backend,
            block_request,
            block_request_generation,
            block_completion,
            block_completion_generation,
            data_digest,
            &note,
        ),
        SemanticCommand::RecordBlockWritePath {
            write_path,
            backend,
            block_request,
            block_request_generation,
            block_completion,
            block_completion_generation,
            payload_digest,
            note,
        } => graph.record_block_write_path_with_id(
            write_path,
            backend,
            block_request,
            block_request_generation,
            block_completion,
            block_completion_generation,
            payload_digest,
            &note,
        ),
        SemanticCommand::RecordBlockRequestQueue {
            queue,
            backend,
            block_device,
            block_device_generation,
            depth,
            entries,
            note,
        } => graph.record_block_request_queue_with_id(
            queue,
            backend,
            block_device,
            block_device_generation,
            depth,
            &entries,
            &note,
        ),
        SemanticCommand::RecordBlockDmaBuffer {
            block_dma_buffer,
            backend,
            block_request,
            block_request_generation,
            dma_buffer,
            dma_buffer_generation,
            buffer_digest,
            note,
        } => graph.record_block_dma_buffer_with_id(
            block_dma_buffer,
            backend,
            block_request,
            block_request_generation,
            dma_buffer,
            dma_buffer_generation,
            buffer_digest,
            &note,
        ),
        SemanticCommand::RecordBlockPageObject {
            block_page_object,
            block_dma_buffer,
            block_dma_buffer_generation,
            block_completion,
            block_completion_generation,
            aspace,
            vma_region,
            page,
            page_dirty_generation,
            page_backing,
            cow_state,
            page_state,
            page_offset,
            byte_len,
            note,
        } => graph.record_block_page_object_with_id(
            block_page_object,
            block_dma_buffer,
            block_dma_buffer_generation,
            block_completion,
            block_completion_generation,
            aspace,
            vma_region,
            page,
            page_dirty_generation,
            page_backing,
            cow_state,
            page_state,
            page_offset,
            byte_len,
            &note,
        ),
        SemanticCommand::RecordBufferCacheObject {
            buffer_cache_object,
            block_page_object,
            block_page_object_generation,
            page,
            page_dirty_generation,
            block_offset,
            byte_len,
            cache_state,
            coherency_epoch,
            note,
        } => graph.record_buffer_cache_object_with_id(
            buffer_cache_object,
            block_page_object,
            block_page_object_generation,
            page,
            page_dirty_generation,
            block_offset,
            byte_len,
            cache_state,
            coherency_epoch,
            &note,
        ),
        SemanticCommand::RecordFileObject {
            file_object,
            buffer_cache_object,
            buffer_cache_object_generation,
            namespace,
            file_key,
            path,
            file_offset,
            byte_len,
            file_size,
            content_digest,
            state,
            note,
        } => graph.record_file_object_with_id(
            file_object,
            buffer_cache_object,
            buffer_cache_object_generation,
            &namespace,
            &file_key,
            &path,
            file_offset,
            byte_len,
            file_size,
            content_digest,
            state,
            &note,
        ),
        SemanticCommand::RecordDirectoryObject {
            directory_object,
            file_object,
            file_object_generation,
            namespace,
            directory_key,
            directory_path,
            entry_name,
            child_file_key,
            child_path,
            entry_kind,
            file_size,
            content_digest,
            state,
            note,
        } => graph.record_directory_object_with_id(
            directory_object,
            file_object,
            file_object_generation,
            &namespace,
            &directory_key,
            &directory_path,
            &entry_name,
            &child_file_key,
            &child_path,
            entry_kind,
            file_size,
            content_digest,
            state,
            &note,
        ),
        SemanticCommand::RecordFatAdapterObject {
            fat_adapter_object,
            directory_object,
            directory_object_generation,
            file_object,
            file_object_generation,
            block_device,
            block_device_generation,
            implementation,
            version,
            profile,
            volume_label,
            image_bytes,
            adapter_path,
            semantic_path,
            bytes_written,
            bytes_read,
            write_digest,
            read_digest,
            file_content_digest,
            state,
            note,
        } => graph.record_fat_adapter_object_with_id(
            fat_adapter_object,
            directory_object,
            directory_object_generation,
            file_object,
            file_object_generation,
            block_device,
            block_device_generation,
            &implementation,
            &version,
            &profile,
            &volume_label,
            image_bytes,
            &adapter_path,
            &semantic_path,
            bytes_written,
            bytes_read,
            write_digest,
            read_digest,
            file_content_digest,
            state,
            &note,
        ),
        SemanticCommand::RecordExt4AdapterObject {
            ext4_adapter_object,
            directory_object,
            directory_object_generation,
            file_object,
            file_object_generation,
            block_device,
            block_device_generation,
            implementation,
            version,
            profile,
            volume_label,
            image_bytes,
            adapter_path,
            semantic_path,
            bytes_read,
            read_digest,
            file_content_digest,
            directory_entries,
            read_only_enforced,
            state,
            note,
        } => graph.record_ext4_adapter_object_with_id(
            ext4_adapter_object,
            directory_object,
            directory_object_generation,
            file_object,
            file_object_generation,
            block_device,
            block_device_generation,
            &implementation,
            &version,
            &profile,
            &volume_label,
            image_bytes,
            &adapter_path,
            &semantic_path,
            bytes_read,
            read_digest,
            file_content_digest,
            directory_entries,
            read_only_enforced,
            state,
            &note,
        ),
        SemanticCommand::RecordFileHandleCapability {
            file_handle_capability,
            owner_store,
            owner_store_generation,
            file_object,
            file_object_generation,
            directory_object,
            directory_object_generation,
            capability,
            capability_generation,
            handle,
            operation,
            file_offset,
            byte_len,
            content_digest,
            note,
        } => graph.record_file_handle_capability_with_id(
            file_handle_capability,
            owner_store,
            owner_store_generation,
            file_object,
            file_object_generation,
            directory_object,
            directory_object_generation,
            capability,
            capability_generation,
            handle,
            &operation,
            file_offset,
            byte_len,
            content_digest,
            &note,
        ),
        SemanticCommand::RecordFsWait {
            fs_wait,
            wait,
            wait_generation,
            file_handle_capability,
            file_handle_capability_generation,
            operation,
            sequence,
            note,
        } => graph.record_fs_wait_with_id(
            fs_wait,
            wait,
            wait_generation,
            file_handle_capability,
            file_handle_capability_generation,
            &operation,
            sequence,
            &note,
        ),
        SemanticCommand::ResolveFsWait { fs_wait, fs_wait_generation, note } => {
            graph.resolve_fs_wait(fs_wait, fs_wait_generation, &note)
        }
        SemanticCommand::CancelFsWait { fs_wait, fs_wait_generation, errno, reason, note } => {
            graph.cancel_fs_wait(fs_wait, fs_wait_generation, errno, reason, &note)
        }
        SemanticCommand::CleanupBlockDriver {
            cleanup,
            io_cleanup,
            block_device,
            block_device_generation,
            backend,
            reason,
            note,
        } => graph.cleanup_block_driver_with_id(
            cleanup,
            io_cleanup,
            block_device,
            block_device_generation,
            backend,
            &reason,
            &note,
        ),
        SemanticCommand::RecordBlockDeviceObject {
            block_device,
            name,
            device,
            device_generation,
            sector_size,
            sector_count,
            read_only,
            max_transfer_sectors,
            note,
        } => graph.record_block_device_object_with_id(
            block_device,
            &name,
            device,
            device_generation,
            sector_size,
            sector_count,
            read_only,
            max_transfer_sectors,
            &note,
        ),
        SemanticCommand::RecordBlockRangeObject {
            block_range,
            block_device,
            block_device_generation,
            start_sector,
            sector_count,
            note,
        } => graph.record_block_range_object_with_id(
            block_range,
            block_device,
            block_device_generation,
            start_sector,
            sector_count,
            &note,
        ),
        SemanticCommand::RecordBlockRequestObject {
            block_request,
            block_device,
            block_device_generation,
            block_range,
            block_range_generation,
            operation,
            sequence,
            note,
        } => graph.record_block_request_object_with_id(
            block_request,
            block_device,
            block_device_generation,
            block_range,
            block_range_generation,
            operation,
            sequence,
            &note,
        ),
        SemanticCommand::RecordBlockCompletionObject {
            block_completion,
            block_request,
            block_request_generation,
            sequence,
            completed_bytes,
            status,
            note,
        } => graph.record_block_completion_object_with_id(
            block_completion,
            block_request,
            block_request_generation,
            sequence,
            completed_bytes,
            status,
            &note,
        ),
        SemanticCommand::RecordBlockWait {
            block_wait,
            wait,
            wait_generation,
            block_request,
            block_request_generation,
            note,
        } => graph.record_block_wait_with_id(
            block_wait,
            wait,
            wait_generation,
            block_request,
            block_request_generation,
            &note,
        ),
        SemanticCommand::ResolveBlockWait {
            block_wait,
            block_wait_generation,
            block_completion,
            block_completion_generation,
            note,
        } => graph.resolve_block_wait_with_completion(
            block_wait,
            block_wait_generation,
            block_completion,
            block_completion_generation,
            &note,
        ),
        SemanticCommand::CancelBlockWait {
            block_wait,
            block_wait_generation,
            errno,
            reason,
            note,
        } => graph.cancel_block_wait(block_wait, block_wait_generation, errno, reason, &note),
        SemanticCommand::ApplyBlockPendingIoPolicy {
            policy,
            block_wait,
            block_wait_generation,
            action,
            retry_request,
            retry_request_generation,
            errno,
            retry_attempt,
            max_retries,
            note,
        } => graph.apply_block_pending_io_policy_with_id(
            policy,
            block_wait,
            block_wait_generation,
            action,
            retry_request,
            retry_request_generation,
            errno,
            retry_attempt,
            max_retries,
            &note,
        ),
        SemanticCommand::RecordBlockRequestGenerationAudit {
            audit,
            block_device,
            block_device_generation,
            block_range,
            block_range_generation,
            block_request,
            block_request_generation,
            backend,
            dma_buffer,
            rejected_completion_generation_probes,
            rejected_wait_generation_probes,
            rejected_dma_generation_probes,
            rejected_queue_generation_probes,
            note,
        } => graph.record_block_request_generation_audit_with_id(
            audit,
            block_device,
            block_device_generation,
            block_range,
            block_range_generation,
            block_request,
            block_request_generation,
            backend,
            dma_buffer,
            rejected_completion_generation_probes,
            rejected_wait_generation_probes,
            rejected_dma_generation_probes,
            rejected_queue_generation_probes,
            &note,
        ),
        SemanticCommand::RecordBlockBenchmark {
            benchmark,
            scenario,
            backend,
            block_device,
            block_device_generation,
            block_range,
            block_range_generation,
            read_path,
            read_path_generation,
            write_path,
            write_path_generation,
            request_queue,
            request_queue_generation,
            block_dma_buffer,
            block_dma_buffer_generation,
            sample_requests,
            sample_bytes,
            read_completed_requests,
            write_completed_requests,
            queue_completed_requests,
            measured_nanos,
            budget_nanos,
            p50_latency_nanos,
            p99_latency_nanos,
            note,
        } => graph.record_block_benchmark_with_id(
            benchmark,
            &scenario,
            backend,
            block_device,
            block_device_generation,
            block_range,
            block_range_generation,
            read_path,
            read_path_generation,
            write_path,
            write_path_generation,
            request_queue,
            request_queue_generation,
            block_dma_buffer,
            block_dma_buffer_generation,
            sample_requests,
            sample_bytes,
            read_completed_requests,
            write_completed_requests,
            queue_completed_requests,
            measured_nanos,
            budget_nanos,
            p50_latency_nanos,
            p99_latency_nanos,
            &note,
        ),
        SemanticCommand::RecordBlockRecoveryBenchmark {
            benchmark,
            scenario,
            cleanup,
            cleanup_generation,
            io_cleanup,
            io_cleanup_generation,
            recovery_start_event,
            recovery_complete_event,
            cancelled_block_waits,
            cancelled_wait_tokens,
            released_dma_buffers,
            revoked_device_capabilities,
            recovery_nanos,
            budget_nanos,
            note,
        } => graph.record_block_recovery_benchmark_with_id(
            benchmark,
            &scenario,
            cleanup,
            cleanup_generation,
            io_cleanup,
            io_cleanup_generation,
            recovery_start_event,
            recovery_complete_event,
            cancelled_block_waits,
            cancelled_wait_tokens,
            released_dma_buffers,
            revoked_device_capabilities,
            recovery_nanos,
            budget_nanos,
            &note,
        ),
        other => return ApplyDispatch::Next(Box::new(other)),
    };
    ApplyDispatch::Applied(applied)
}
